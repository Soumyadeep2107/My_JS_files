// Different types of data types
var x="hello World"; //string
console.log(typeof(x));

var x='h'; //string
console.log(typeof(x));

var x="25"; //string
console.log(typeof(x));

var x=25; // number
console.log(typeof(x));

var x=true;  //boolean
console.log(typeof(x));

var x=["HTML","CSS","JS"]; //Array but in java script it is asummed as object.
console.log(typeof(x));

var x={first:"jane",last:"Doe"}; //Object
console.log(typeof(x));

var y=null; //data type is null
console.log(typeof(y));

var y;  //Undefined
console.log(typeof(y));
